// Type filter functionality
document.addEventListener('DOMContentLoaded', function() {
    const typeFilters = document.querySelectorAll('.type-filter');
    const maleModels = document.querySelectorAll('.male-model-item');
    
    typeFilters.forEach(filter => {
        filter.addEventListener('click', function() {
            // Remove active class from all filters
            typeFilters.forEach(f => f.classList.remove('active'));
            // Add active class to clicked filter
            this.classList.add('active');
            
            const selectedType = this.getAttribute('data-type');
            
            // Filter models
            maleModels.forEach(model => {
                if (selectedType === 'all' || model.getAttribute('data-type') === selectedType) {
                    model.style.display = 'block';
                } else {
                    model.style.display = 'none';
                }
            });
        });
    });
});

// Search functionality
function searchMaleModels() {
    const searchTerm = document.getElementById('maleSearch').value.toLowerCase();
    const maleModels = document.querySelectorAll('.male-model-item');
    
    maleModels.forEach(model => {
        const modelText = model.textContent.toLowerCase();
        if (modelText.includes(searchTerm)) {
            model.style.display = 'block';
        } else {
            model.style.display = 'none';
        }
    });
}

