// Age range filter functionality
document.addEventListener('DOMContentLoaded', function() {
    const ageFilters = document.querySelectorAll('.age-range-filter');
    const matureModels = document.querySelectorAll('.mature-model-item');
    
    ageFilters.forEach(filter => {
        filter.addEventListener('click', function() {
            // Remove active class from all filters
            ageFilters.forEach(f => f.classList.remove('active'));
            // Add active class to clicked filter
            this.classList.add('active');
            
            const selectedAge = this.getAttribute('data-age');
            
            // Filter models
            matureModels.forEach(model => {
                if (selectedAge === 'all' || model.getAttribute('data-age') === selectedAge) {
                    model.style.display = 'block';
                } else {
                    model.style.display = 'none';
                }
            });
        });
    });
});

// Search functionality
function searchMatureModels() {
    const searchTerm = document.getElementById('matureSearch').value.toLowerCase();
    const matureModels = document.querySelectorAll('.mature-model-item');
    
    matureModels.forEach(model => {
        const modelText = model.textContent.toLowerCase();
        if (modelText.includes(searchTerm)) {
            model.style.display = 'block';
        } else {
            model.style.display = 'none';
        }
    });
}

