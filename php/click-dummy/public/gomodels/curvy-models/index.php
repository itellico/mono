// Size filter functionality
document.addEventListener('DOMContentLoaded', function() {
    const sizeFilters = document.querySelectorAll('.size-filter');
    const curvyModels = document.querySelectorAll('.curvy-model-item');
    
    sizeFilters.forEach(filter => {
        filter.addEventListener('click', function() {
            // Remove active class from all filters
            sizeFilters.forEach(f => f.classList.remove('active'));
            // Add active class to clicked filter
            this.classList.add('active');
            
            const selectedSize = this.getAttribute('data-size');
            
            // Filter models
            curvyModels.forEach(model => {
                if (selectedSize === 'all' || model.getAttribute('data-size') === selectedSize) {
                    model.style.display = 'block';
                } else {
                    model.style.display = 'none';
                }
            });
        });
    });
});

// Search functionality
function searchCurvyModels() {
    const searchTerm = document.getElementById('curvySearch').value.toLowerCase();
    const curvyModels = document.querySelectorAll('.curvy-model-item');
    
    curvyModels.forEach(model => {
        const modelText = model.textContent.toLowerCase();
        if (modelText.includes(searchTerm)) {
            model.style.display = 'block';
        } else {
            model.style.display = 'none';
        }
    });
}

// Load more functionality
function loadMoreCurvyModels() {
