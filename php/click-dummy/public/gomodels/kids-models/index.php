// Age group filter functionality
document.addEventListener('DOMContentLoaded', function() {
    const ageFilters = document.querySelectorAll('.age-group-filter');
    const kidsModels = document.querySelectorAll('.kids-model-item');
    
    ageFilters.forEach(filter => {
        filter.addEventListener('click', function() {
            // Remove active class from all filters
            ageFilters.forEach(f => f.classList.remove('active'));
            // Add active class to clicked filter
            this.classList.add('active');
            
            const selectedAge = this.getAttribute('data-age');
            
            // Filter models
            kidsModels.forEach(model => {
                if (selectedAge === 'all' || model.getAttribute('data-age') === selectedAge) {
                    model.style.display = 'block';
                } else {
                    model.style.display = 'none';
                }
            });
        });
    });
});

// Search functionality
function searchKidsModels() {
    const searchTerm = document.getElementById('kidsSearch').value.toLowerCase();
    const kidsModels = document.querySelectorAll('.kids-model-item');
    
    kidsModels.forEach(model => {
        const modelText = model.textContent.toLowerCase();
        if (modelText.includes(searchTerm)) {
            model.style.display = 'block';
        } else {
            model.style.display = 'none';
        }
    });
}

// Load more functionality
function loadMoreKids() {
    alert('Loading more kids models... (This would load additional models in a real application)');
}

// Search on Enter key
document.getElementById('kidsSearch').addEventListener('keypress', function(e) {
    if (e.key === 'Enter') {
        searchKidsModels();
    }
});

// Smooth scrolling for anchor links
document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', function (e) {
        e.preventDefault();
        const target = document.querySelector(this.getAttribute('href'));
        if (target) {
            target.scrollIntoView({
                behavior: 'smooth',
                block: 'start'
            });
        }
    });
});
